public class Bruker {
    public String epost;
    public String passord;
    public String fnavn;
    public String enavn;
    public String adresse;
    public int mobilnr;

    public Bruker(String epost, String passord, String fnavn, String enavn, String adresse, int mobilnr){
        this.epost=epost;
        this.passord=passord;
        this.fnavn=fnavn;
        this.enavn=enavn;
        this.adresse=adresse;
        this.mobilnr=mobilnr;

    }
}
